// returns true if available, false if sold out
export function cycleCotas(cabana) {
    let available = 0;
    let status = 'v';

    if (cabana.cotas) {
        for(var i = 0; i < cabana.cotas.length; i++) {
            let cota = cabana.cotas[i];
            
            if (cota.status === 'd') {
                if(status !== 'r') status = 'd';
                available++;
            }
            if (cota.status === 'r') {
                status = 'r';
            }
        }
    }

    return {status, available}
}

